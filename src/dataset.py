"""
dataset.py - Dataset loading

Supports two types of data:
  1. AEC-Challenge synthetic dataset (Microsoft open dataset)
     Directory structure:
       data/aec_challenge/synthetic/
         ├── nearend_mic_signal/   nearend_mic_fileid_*.wav  (microphone signal, contains echo)
         ├── farend_speech/        farend_speech_fileid_*.wav (far-end / reference signal)
         ├── nearend_speech/       nearend_speech_fileid_*.wav (clean target, supervision label)
         └── meta.csv

  2. Simulated data (generated by generate_sim_data.py)
     Directory structure:
       data/simulated/
         ├── mic/      mic_*.wav
         ├── ref/      ref_*.wav
         └── clean/    clean_*.wav

Output format:
  mic_wav:   [L]  microphone time-domain signal (echo + noise)
  ref_wav:   [L]  far-end reference signal
  clean_wav: [L]  clean target speech (training label)
"""

import os
import glob
import random
import numpy as np
import torch
import torchaudio
import torchaudio.transforms as T
from torch.utils.data import Dataset, DataLoader, ConcatDataset
from typing import Optional, List, Tuple
import pandas as pd


SAMPLE_RATE = 16000
CLIP_DURATION = 4.0          # duration of each training sample (seconds)
CLIP_LEN = int(SAMPLE_RATE * CLIP_DURATION)  # 64000 samples


def load_wav(path: str, target_sr: int = SAMPLE_RATE) -> torch.Tensor:
    """Load a wav file, resample to the target sample rate, return [L] mono."""
    import soundfile as sf
    data, sr = sf.read(path, dtype='float32', always_2d=False)
    wav = torch.from_numpy(data)
    if wav.ndim == 2:
        wav = wav.mean(dim=-1)   # multi-channel → mono
    if sr != target_sr:
        wav = wav.unsqueeze(0)
        wav = torchaudio.functional.resample(wav, sr, target_sr)
        wav = wav.squeeze(0)
    return wav   # [L]


def pad_or_crop(wav: torch.Tensor, length: int) -> torch.Tensor:
    """Crop or zero-pad wav to the specified length."""
    if wav.shape[0] >= length:
        start = random.randint(0, wav.shape[0] - length)
        return wav[start: start + length]
    else:
        pad_len = length - wav.shape[0]
        return torch.nn.functional.pad(wav, (0, pad_len))


def normalize(wav: torch.Tensor, eps: float = 1e-8) -> torch.Tensor:
    """Peak normalization."""
    peak = wav.abs().max()
    return wav / (peak + eps)


class AECChallengeDataset(Dataset):
    """
    AEC-Challenge synthetic dataset

    Args:
        data_dir:  Root data directory containing nearend_mic_signal/ farend_speech/ nearend_speech/
        split:     'train' or 'val'
        val_ratio: Validation set ratio, default 0.05
        clip_len:  Sample length (samples)
        augment:   Whether to apply data augmentation
    """

    def __init__(self,
                 data_dir: str,
                 split: str = 'train',
                 val_ratio: float = 0.05,
                 clip_len: int = CLIP_LEN,
                 augment: bool = True):
        self.data_dir = data_dir
        self.split = split
        self.clip_len = clip_len
        self.augment = augment and (split == 'train')

        mic_dir   = os.path.join(data_dir, 'nearend_mic_signal')
        ref_dir   = os.path.join(data_dir, 'farend_speech')
        clean_dir = os.path.join(data_dir, 'nearend_speech')

        # collect all fileids
        mic_files = sorted(glob.glob(os.path.join(mic_dir, '*.wav')))
        self.samples = []

        for mic_path in mic_files:
            basename = os.path.basename(mic_path)
            # nearend_mic_fileid_123.wav → 123
            fileid = basename.replace('nearend_mic_fileid_', '').replace('.wav', '')
            ref_path   = os.path.join(ref_dir,   f'farend_speech_fileid_{fileid}.wav')
            clean_path = os.path.join(clean_dir, f'nearend_speech_fileid_{fileid}.wav')

            if os.path.exists(ref_path) and os.path.exists(clean_path):
                self.samples.append((mic_path, ref_path, clean_path))

        if not self.samples:
            print(f'[Warning] AECChallengeDataset: no valid samples found in {data_dir}')

        # train/val split
        random.seed(42)
        random.shuffle(self.samples)
        n_val = max(1, int(len(self.samples) * val_ratio))
        if split == 'val':
            self.samples = self.samples[:n_val]
        else:
            self.samples = self.samples[n_val:]

        print(f'[AECChallenge] {split}: {len(self.samples)} samples')

    def __len__(self) -> int:
        return len(self.samples)

    def __getitem__(self, idx: int) -> Tuple[torch.Tensor, torch.Tensor, torch.Tensor]:
        mic_path, ref_path, clean_path = self.samples[idx]

        mic   = load_wav(mic_path)
        ref   = load_wav(ref_path)
        clean = load_wav(clean_path)

        # align lengths
        min_len = min(mic.shape[0], ref.shape[0], clean.shape[0])
        mic   = mic[:min_len]
        ref   = ref[:min_len]
        clean = clean[:min_len]

        # crop / pad
        mic   = pad_or_crop(mic,   self.clip_len)
        ref   = pad_or_crop(ref,   self.clip_len)
        clean = pad_or_crop(clean, self.clip_len)

        # data augmentation
        if self.augment:
            mic, ref, clean = self._augment(mic, ref, clean)

        # normalize
        mic   = normalize(mic)
        ref   = normalize(ref)
        clean = normalize(clean)

        return mic, ref, clean

    def _augment(self, mic, ref, clean):
        # random volume perturbation (±6dB)
        gain = 10 ** (random.uniform(-0.5, 0.5))
        mic   = mic   * gain
        ref   = ref   * gain
        clean = clean * gain

        # randomly add low-power white noise
        if random.random() < 0.3:
            noise_level = random.uniform(0.001, 0.01)
            mic = mic + torch.randn_like(mic) * noise_level

        return mic, ref, clean


class SimulatedDataset(Dataset):
    """
    Simulated dataset (generated by generate_sim_data.py)

    Directory structure:
        data_dir/
          mic/   mic_000001.wav ...
          ref/   ref_000001.wav ...
          clean/ clean_000001.wav ...
    """

    def __init__(self,
                 data_dir: str,
                 split: str = 'train',
                 val_ratio: float = 0.05,
                 clip_len: int = CLIP_LEN,
                 augment: bool = True):
        self.data_dir = data_dir
        self.split = split
        self.clip_len = clip_len
        self.augment = augment and (split == 'train')

        mic_files = sorted(glob.glob(os.path.join(data_dir, 'mic', '*.wav')))
        self.samples = []

        for mic_path in mic_files:
            basename = os.path.basename(mic_path)
            stem = basename.replace('mic_', '').replace('.wav', '')
            ref_path   = os.path.join(data_dir, 'ref',   f'ref_{stem}.wav')
            clean_path = os.path.join(data_dir, 'clean', f'clean_{stem}.wav')

            if os.path.exists(ref_path) and os.path.exists(clean_path):
                self.samples.append((mic_path, ref_path, clean_path))

        if not self.samples:
            print(f'[Warning] SimulatedDataset: no valid samples found in {data_dir}')

        random.seed(42)
        random.shuffle(self.samples)
        n_val = max(1, int(len(self.samples) * val_ratio))
        if split == 'val':
            self.samples = self.samples[:n_val]
        else:
            self.samples = self.samples[n_val:]

        print(f'[Simulated] {split}: {len(self.samples)} samples')

    def __len__(self) -> int:
        return len(self.samples)

    def __getitem__(self, idx: int) -> Tuple[torch.Tensor, torch.Tensor, torch.Tensor]:
        mic_path, ref_path, clean_path = self.samples[idx]

        mic   = load_wav(mic_path)
        ref   = load_wav(ref_path)
        clean = load_wav(clean_path)

        min_len = min(mic.shape[0], ref.shape[0], clean.shape[0])
        mic, ref, clean = mic[:min_len], ref[:min_len], clean[:min_len]

        mic   = pad_or_crop(mic,   self.clip_len)
        ref   = pad_or_crop(ref,   self.clip_len)
        clean = pad_or_crop(clean, self.clip_len)

        if self.augment:
            mic, ref, clean = self._augment(mic, ref, clean)

        mic   = normalize(mic)
        ref   = normalize(ref)
        clean = normalize(clean)

        return mic, ref, clean

    def _augment(self, mic, ref, clean):
        gain = 10 ** (random.uniform(-0.5, 0.5))
        mic, ref, clean = mic * gain, ref * gain, clean * gain
        if random.random() < 0.3:
            noise_level = random.uniform(0.001, 0.01)
            mic = mic + torch.randn_like(mic) * noise_level
        return mic, ref, clean


def build_dataloaders(
    aec_dir: Optional[str] = None,
    sim_dir: Optional[str] = None,
    batch_size: int = 16,
    num_workers: int = 4,
    clip_len: int = CLIP_LEN,
) -> Tuple[DataLoader, DataLoader]:
    """
    Build train and validation DataLoaders.
    At least one data directory must be provided.

    Args:
        aec_dir:     AEC-Challenge data directory (optional)
        sim_dir:     Simulated data directory (optional)
        batch_size:  Batch size
        num_workers: Number of data loading workers
        clip_len:    Sample length

    Returns:
        train_loader, val_loader
    """
    train_datasets = []
    val_datasets   = []

    if aec_dir and os.path.exists(aec_dir):
        train_datasets.append(AECChallengeDataset(aec_dir, 'train', clip_len=clip_len, augment=True))
        val_datasets.append(  AECChallengeDataset(aec_dir, 'val',   clip_len=clip_len, augment=False))

    if sim_dir and os.path.exists(sim_dir):
        train_datasets.append(SimulatedDataset(sim_dir, 'train', clip_len=clip_len, augment=True))
        val_datasets.append(  SimulatedDataset(sim_dir, 'val',   clip_len=clip_len, augment=False))

    assert train_datasets, 'At least one valid data directory must be provided (aec_dir or sim_dir)'

    train_set = ConcatDataset(train_datasets)
    val_set   = ConcatDataset(val_datasets)

    train_loader = DataLoader(
        train_set,
        batch_size=batch_size,
        shuffle=True,
        num_workers=num_workers,
        pin_memory=False,
        drop_last=True,
    )
    val_loader = DataLoader(
        val_set,
        batch_size=batch_size,
        shuffle=False,
        num_workers=num_workers,
        pin_memory=False,
    )

    print(f'Train set: {len(train_set)} samples  Val set: {len(val_set)} samples')
    return train_loader, val_loader


